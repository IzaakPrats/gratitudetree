import { Box, Flex, Heading, Text } from "@chakra-ui/react";
import type { NextPage } from "next";
import Head from "next/head";
import { useEffect, useState } from "react";
import { toast, ToastContainer } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import { useAccount, useNetwork } from "wagmi";
import Account from "../components/Account";
import Connect from "../components/Connect";
import GratitudeNftContractInfo from "../components/GratitudeNFTContractInfo";
import GratitudeNftMintContainer from "../components/GratitudeNftMintContainer";
import Network from "../components/Network";
import { getShortAddress } from "../utils";

const Home: NextPage = () => {
  const [{ data: accountData }] = useAccount();
  const [{ data: networkData }] = useNetwork();
  const [currentAccount, setCurrentAccount] = useState<string | undefined>("");

  useEffect(() => {
    setCurrentAccount(accountData?.address);
  }, []);

  useEffect(() => {
    const accountAddress = accountData?.address;
    if (accountAddress && accountAddress !== currentAccount) {
      setCurrentAccount(accountAddress);
      toast(
        `Account switched to ${getShortAddress(accountAddress) ?? "undefined"}`,
        { autoClose: 2000 }
      );
    }
  }, [accountData]);

  return (
    <div>
      <Head>
        <title>Your DApp</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Flex direction="column" w="100%" p={4} align="center" justify="center">
        <ToastContainer />
        <Heading fontSize={"5xl"} textAlign="center">
          Gratitude Tree
        </Heading>
        {accountData ? (
          <Box mt={8} bg="lightgray" w="100%" p={4} color="black">
            <Account />
            <Network />
          </Box>
        ) : (
          <Connect />
        )}
        {accountData && !networkData?.chain?.unsupported && (
          <Flex direction="column" align="center" justify="center" w="100%">
            <Box bg="lavender" w="100%" p={4} color="black">
              <GratitudeNftContractInfo />
            </Box>
            <GratitudeNftMintContainer />
          </Flex>
        )}
      </Flex>
    </div>
  );
};

export default Home;
